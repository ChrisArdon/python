states_of_america = ["Delaware", "Pennsylvania", "New Jersey", "Georgia", "Connecticut", "Massachusetts", "Maryland", "South Carolina", "New Hampshire"]

#Print an element from the list given the index
print(states_of_america[3])

#We can also use negative indexes, which starts from the end
print(states_of_america[-1])

#We can modify an element from the list
states_of_america[1] = "Pencilvania"

#Add a new object to the end of a list
states_of_america.append("Andrea Elias Island")

print(states_of_america)